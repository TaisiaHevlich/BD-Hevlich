#include <iostream>

using namespace std;

int main() {
  const int n = 6;
  double x[n] = {3.0, 4.05, 5.1, 6.15, 7.2, 8.25};
  double y[n] = {2.4, 3.05, -1.26, 0.7, 3.38, 5.79};

  // Коефіцієнти для першого інтерполяційного полінома Ньютона
  double a[n];
  a[0] = y[0];
  for (int j = 1; j < n; j++) {
    for (int i = n-1; i >= j; i--) {
      y[i] = (y[i] - y[i-1]) / (x[i] - x[i-j]);
    }
    a[j] = y[j];
  }

  // Коефіцієнти для другого інтерполяційного полінома Ньютона
  double a2[n];
  a2[0] = y[n-1];
  for (int j = 1; j < n; j++) {
    for (int i = n-1; i >= j; i--) {
      y[i] = (y[i] - y[i-1]) / (x[i] - x[i-j]);
    }
    a2[j] = y[n-1-j];
  }

  // Виведення коефіцієнтів поліномів
  cout << "Коефіцієнти першого інтерполяційного полінома Ньютона: ";
  for (int i = 0; i < n; i++) {
    cout << a[i] << " ";
  }
  cout << endl;

  cout << "Коефіцієнти другого інтерполяційного полінома Ньютона: ";
  for (int i = 0; i < n; i++) {
    cout << a2[i] << " ";
  }
  cout << endl;

  // Значення поліномів в точці
  double x4 = 6.15; // Задана точка x4
  double p = a[n-1];
  for (int i = n-2; i >= 0; i--) {
    p = a[i] + (x4 - x[i]) * p;
  }

  double p2 = a2[n-1];
  for (int i = n-2; i >= 0; i--) {
    p2 = a2[i] + (x4 - x[n-1-i]) * p2;
  }

  cout << "Значення першого полінома N5(" << x4 << ") = " << p << endl;
  cout << "Значення другого полінома N5*(" << x4 << ") = " << p2 << endl;

  return 0;
}
